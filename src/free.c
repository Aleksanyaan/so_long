#include "../includes/so_long.h"

void	free_map(char **map)
{
	int	i;

	if (!map)
		return ;
	i = 0;
	while (map[i])
		free(map[i++]);
	free(map);
}

void	free_all(t_game *game)
{
	free(game->map);
	free(game->player);
	free(game->img);
	free(game);
}
